package homework;

public class Pro5and6datatypesandarithmatic {
    public static void main(String[] args){
        int A = 25;
        int B = 38;
        System.out.println(A+B);
        System.out.println(A-B);
        System.out.println(A*B);
        System.out.println(A/B);
        System.out.println(A++);
        System.out.println(B--);
        System.out.println(--A);
        System.out.println(--B);
    }
}
